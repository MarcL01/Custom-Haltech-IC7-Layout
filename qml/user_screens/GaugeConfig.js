function initColorPalette(g, qml, t)
{
    var j;
    if(t == 'gear') t = 'numeric';
    var offset = ColorPaletteParams.offset[t];
    var length = ColorPaletteParams.length[t];
    var screen = qml.replace('.qml', '');
    var p = ColorPaletteParams.palette[screen];
    for (j = offset; j < offset + length; j++)
    {
        var k = ColorPaletteParams.vars[j];
        if (g.hasOwnProperty(k)) g[k] = p[j];
    }
    offset = ColorPaletteParams.offset['screen'];
    length = ColorPaletteParams.length['screen'];
    for (j = offset; j < offset + length; j++)
    {
        var k = ColorPaletteParams.vars[j];
        if (topLevelItem.hasOwnProperty(k)) topLevelItem[k] = p[j];
    }
}
